    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        if (isEmpty()) {
            int id = Integer.parseInt(jTextField1.getText());
            String pname = jTextField2.getText();
            String cname = jComboBox1.getSelectedItem().toString();
            int qty = Integer.parseInt(jTextField3.getText());
            if (isNumeric(jTextField4.getText())) {
                double price = Double.parseDouble(jTextField4.getText());
                if (!product.isProductIdExist(id)) {
                    if (!product.isProductAndCategoryExist(pname, cname)) {
                        product.insert(pname, cname, qty, price);
                        jTable1.setModel(new DefaultTableModel(null, new Object[]{"Product ID", "Product Name", "Categoty", "Quantity", "Price"}));
                        product.getProductsInfo(jTable1, "");
                        clear();
                    } else {
                        JOptionPane.showMessageDialog(this, "Product and catgory already exists!", "Warning", 2);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Product ID already exists!", "Warning", 2);

                }
            }
        }
    }//GEN-LAST:event_jButtonSaveActionPerformed

