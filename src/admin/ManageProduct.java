    private void init() {
        jTextField1.setBackground(notEdit);
        jTextField1.setText(String.valueOf(product.getMaxRow()));
        categories = new String[product.numCategories()];
        setCategories();
        jComboBox1.setSelectedIndex(-1);
    }

    private void setCategories() {
        categories = product.getCategories();
        for (String s : categories) {
            jComboBox1.addItem(s);
        }
    }

    private void productTable() {
        product.getProductsInfo(jTable1, "");
        model = (DefaultTableModel) jTable1.getModel();
        jTable1.setRowHeight(30);
        jTable1.setShowGrid(true);
        jTable1.setGridColor(Color.BLACK);
        jTable1.setBackground(Color.WHITE);
        jTable1.setSelectionBackground(Color.LIGHT_GRAY);
    }

    private void clear() {
        jTextField1.setText(String.valueOf(product.getMaxRow()));
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("0");
        jTextField4.setText("0.0");
        jTextField5.setText("");
        jComboBox1.setSelectedIndex(-1);
        jTable1.clearSelection();
    }

    private boolean isEmpty() {
        if (jTextField2.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Product name is required!", "Warning", 2);
            return false;
        }

        if (Integer.parseInt(jTextField3.getText()) <= 0) {
            JOptionPane.showMessageDialog(this, "Please increase the product quantity!", "Warning", 2);
            return false;
        }
        if (jTextField5.getText().equals("0.0")) {
            JOptionPane.showMessageDialog(this, "Product price is required!", "Warning", 2);
            return false;
        }
        return true;
    }

    private boolean check() {
        String newProduct = jTextField2.getText();
        String oldProduct = model.getValueAt(rowIndex, 1).toString();
        String newCategory = jComboBox1.getSelectedItem().toString();
        String oldCategory = model.getValueAt(rowIndex, 2).toString();
        if (newCategory.equals(oldCategory) && newProduct.equals(oldProduct)) {
            return false;
        } else {
            boolean x = product.isProductAndCategoryExist(newProduct, newCategory);
            if (x) {
                JOptionPane.showMessageDialog(this, "Product name and Category name already exists!", "Warning", 2);
            }
            return x;
        }
    }

